package net.alagris.src.controller;

public class ControllersBean {

    private String loginPath;
    private String registerPath;
    private String loginSubmit;
    private String registerSubmit;
    public String getLoginPath() {
        return loginPath;
    }
    public void setLoginPath(String loginPath) {
        this.loginPath = loginPath;
    }
    public String getRegisterPath() {
        return registerPath;
    }
    public void setRegisterPath(String registerPath) {
        this.registerPath = registerPath;
    }
    public String getLoginSubmit() {
        return loginSubmit;
    }
    public void setLoginSubmit(String loginSubmit) {
        this.loginSubmit = loginSubmit;
    }
    public String getRegisterSubmit() {
        return registerSubmit;
    }
    public void setRegisterSubmit(String registerSubmit) {
        this.registerSubmit = registerSubmit;
    }
}
